    public ModelDetailsUI() throws SQLException {
        DBAccessJava dbaccess = DBAccessJava.getDbCon();
        FileReader selectedFile = null;
        try {
            initComponents();
            setWindowIcon();
            selectedFile = new FileReader("src/prediction/apparel/csv/metricsData1.csv");
            CSVReader reader = new CSVReader(selectedFile);
            String[] headers = new String[]{"Model Name","Accuracy","Precision", "Recall", "F1"};
            DefaultTableModel model = new DefaultTableModel(headers,0);
            
            List<String[]> tableEntries = reader.readAll();
            int size = tableEntries.size();
            //Fill table entries and update progressbar
            for(int i = 1; i < size; i++){
                model.addRow(tableEntries.get(i));
            }
            dataMeticsTbl.setModel(model);
            
            String sqlread="SELECT * FROM `modeldetails`"; 
            ResultSet rs=dbaccess.query(sqlread);
            String bestmodel=null;
            float Accuracy=0;
            
            while (rs.next()) {
            bestmodel= rs.getString("Model");
            Accuracy = rs.getFloat("Accuracy");
            }
            
            accurateModel_lbl.setText(bestmodel);
            accuracy_lbl.setText(Float.toString(Accuracy));

        } catch (FileNotFoundException ex) {
            Logger.getLogger(ModelDetailsUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ModelDetailsUI.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                selectedFile.close();
            } catch (IOException ex) {
                Logger.getLogger(ModelDetailsUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
    }
    
